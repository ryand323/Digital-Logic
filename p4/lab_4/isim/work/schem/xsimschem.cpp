class _top : public HSim__s6 {
public:
    _top() : HSim__s6(false, "_top", "_top", 0, 0, HSim::VerilogModule) {}
    HSimConfigDecl * topModuleInstantiate() {
        HSimConfigDecl * cfgvh = 0;
        cfgvh = new HSimConfigDecl("default");
        (*cfgvh).registerFuseLibList("unisims_ver;xilinxcorelib_ver");

        (*cfgvh).addVlogModule("work","schem", (HSimInstFactoryPtr)IF0);
        (*cfgvh).addVlogModule("unisims_ver","FMAP", (HSimInstFactoryPtr)IF1);
        (*cfgvh).addVlogModule("unisims_ver","OR3", (HSimInstFactoryPtr)IF2);
        (*cfgvh).addVlogModule("unisims_ver","OR4", (HSimInstFactoryPtr)IF3);
        (*cfgvh).addVlogModule("unisims_ver","AND2", (HSimInstFactoryPtr)IF4);
        (*cfgvh).addVlogModule("unisims_ver","AND3", (HSimInstFactoryPtr)IF5);
        (*cfgvh).addVlogModule("unisims_ver","INV", (HSimInstFactoryPtr)IF6);
        (*cfgvh).addVlogModule("unisims_ver","OR5", (HSimInstFactoryPtr)IF7);
        (*cfgvh).addVlogModule("unisims_ver","OR4", (HSimInstFactoryPtr)IF8);
        (*cfgvh).addVlogModule("unisims_ver","OR3", (HSimInstFactoryPtr)IF9);
        (*cfgvh).addVlogModule("unisims_ver","FMAP", (HSimInstFactoryPtr)IF10);
        (*cfgvh).addVlogModule("work","OR6_MXILINX_schem", (HSimInstFactoryPtr)IF11);
        (*cfgvh).addVlogModule("unisims_ver","OR5", (HSimInstFactoryPtr)IF12);
        (*cfgvh).addVlogModule("unisims_ver","OR4", (HSimInstFactoryPtr)IF13);
        (*cfgvh).addVlogModule("unisims_ver","INV", (HSimInstFactoryPtr)IF14);
        (*cfgvh).addVlogModule("unisims_ver","AND3", (HSimInstFactoryPtr)IF15);
        (*cfgvh).addVlogModule("unisims_ver","AND2", (HSimInstFactoryPtr)IF16);
        (*cfgvh).addVlogModule("work","glbl", (HSimInstFactoryPtr)IF17);
        HSim__s5 * topvl = 0;
        extern HSim__s6 * createworkMschem(const char*);
        topvl = (HSim__s5*)createworkMschem("schem");
        topvl->moduleInstantiate(cfgvh);
        addChild(topvl);
        extern HSim__s6 * createworkMglbl(const char*);
        topvl = (HSim__s5*)createworkMglbl("glbl");
        topvl->moduleInstantiate(cfgvh);
        addChild(topvl);
        return cfgvh;
}
};

main(int argc, char **argv) {
  HSimDesign::initDesign();
  globalKernel->getOptions(argc,argv);
  HSim__s6 * _top_i = 0;
  try {
    HSimConfigDecl *cfg;
 _top_i = new _top();
  cfg =  _top_i->topModuleInstantiate();
    return globalKernel->runTcl(cfg, _top_i, "_top", argc, argv);
  }
  catch (HSimError& msg){
    try {
      globalKernel->error(msg.ErrMsg);
      return 1;
    }
    catch(...) {}
      return 1;
  }
  catch (...){
    globalKernel->fatalError();
    return 1;
  }
}
